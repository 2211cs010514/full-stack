// factorial of 5
function factorial(number) {
    let result = 1;

    for (let i = 1; i <= number; i++) {
        result *= i; // Multiply result by the current value of i
    }

    return result;
}

const number = 5;
console.log(`The factorial of ${number} is: ${factorial(number)}`);


